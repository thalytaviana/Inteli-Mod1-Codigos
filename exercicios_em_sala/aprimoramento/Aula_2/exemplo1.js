// Exemplo 1: Verificar se um número é positivo, negativo ou zero
//Neste exemplo, vamos verificar se um número é positivo, negativo ou zero e exibir uma mensagem de acordo com o resultado:

let numero = 20;

if (numero > 0) {
  console.log(numero + " é um número positivo");
} else if (numero < 0) {
  console.log(numero + " é um número negativo");
} else {
  console.log("O número é zero");
}
