//Exemplo 2: Verificar se um número é par ou ímpar
//Neste exemplo, vamos verificar se um número é par ou ímpar e exibir uma mensagem correspondente:

let numero = 7;

if (numero % 2 === 0) { //porcentagem indica o resto da divisão
  console.log(numero + " é um número par");
} else {
  console.log(numero + " é um número ímpar");
}