// Exemplo 5: Verificar se uma pessoa pode dirigir
//Neste exemplo, vamos verificar se uma pessoa é maior de idade e, portanto, pode dirigir:

let idade = 20;
const idadeMinimaParaDirigir = 18;

if (idade >= idadeMinimaParaDirigir) {
  console.log("Você pode dirigir!");
} else {
  console.log("Você ainda é menor de idade para dirigir.");
}
